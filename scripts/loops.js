// Loops
// 1. Initialization
// 2. Condition
// 3. Increment / Decrement

console.log("----------- While Loop ----------------");

let i = 1;

while (i <= 10) {
  console.log(i++);
}

console.log("----------- Do While Loop ----------------");

let j = 11;

do {
  console.log(i++);
} while (j <= 10);

console.log("----------- For Loop ---------------");

for (let k = 1; k <= 10; k++) {
  console.log(k);
}

